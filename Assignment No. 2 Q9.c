#include<stdio.h>
int main()
{
    int i;
    float f;
    char ch;
    double d;
    printf("The size of Integer-type variable : %d.\n",sizeof(i));
    printf("The size of Float-type variable : %d.\n",sizeof(f));
    printf("The size of Character-type variable : %d.\n",sizeof(ch));
    printf("The size of Double-type variable : %d.\n",sizeof(d));
    return 0;
}